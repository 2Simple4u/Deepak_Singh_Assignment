function rev() {
  
    // Original Array
    var arr = ["Iti","Swati","Sama","Shivesh"];
    document.write("Before reversing of Array is: ",arr);
  
    // Reversed array
    var new_arr = arr.reverse();
    document.write("<br>","<br>");
    document.write("After Reverse of array is: ",new_arr);
}
rev();
